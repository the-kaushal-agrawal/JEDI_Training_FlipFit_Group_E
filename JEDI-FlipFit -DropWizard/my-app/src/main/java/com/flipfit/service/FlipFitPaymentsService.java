package com.flipfit.service;

import com.flipfit.model.FlipFitPayments;

/**
 * Interface for payment-related operations.
 * Provides methods for collecting and validating card details, requesting OTPs, and processing payments.
 *
 */
public interface FlipFitPaymentsService {

    /**
     * Validates the card details provided by the user.
     *
     * @param payments the FlipFitPayments object containing card details to be validated
     * @return true if the card details are valid; false otherwise
     */
    boolean validateCardDetails(FlipFitPayments payments);

    /**
     * Generates a random OTP (One Time Password) for payment verification.
     *
     * @param payments the FlipFitPayments object (used for passing payment details if needed)
     * @return the generated OTP
     */
    int requestOTP(FlipFitPayments payments);

    /**
     * Validates the provided OTP against the server-generated OTP.
     *
     * @param serverOTP the OTP generated by the server for verification
     * @return true if the entered OTP matches the server-generated OTP; false otherwise
     */
    boolean validateOTP(int serverOTP);

    /**
     * Collects card details from the user.
     *
     * @return true if card details were collected successfully; false otherwise
     */
    boolean collectCardDetails();

    boolean validateCardDetails();

    /**
     * Requests an OTP (One Time Password) for payment verification.
     *
     */
    void requestOTP();

    /**
     * Processes the payment based on the collected and validated card details.
     *
     */
    void processPayments();
}
